.global main

main:
    // x1 - адрес начала массива
    // x2 - количество элементов в массиве
    // x3 - для хранения суммы элементов массива

    mov x3, #0         // Инициализируем сумму как 0

sum_loop:
    cbz x2, end_loop   // Если количество элементов равно 0, выходим из цикла
    ldr x4, [x1], #8   // Загружаем элемент массива в x4 и увеличиваем x1 на 8 (размер 64-битного элемента)
    add x3, x3, x4     // Добавляем элемент к сумме
    sub x2, x2, #1     // Уменьшаем счетчик элементов
    b sum_loop         // Переходим к следующей итерации цикла

end_loop:
    // x3 содержит сумму элементов массива
    ret                 // Возвращаем управление
